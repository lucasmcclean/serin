#[derive(Clone, Debug, PartialEq)]
pub enum Token {
    Integer(i64),
    Boolean(bool),
    Identifier(String),
    Let,
    In,
    If,
    Then,
    Else,
    Not,
    Backslash,
    Arrow,
    Equal,
    LeftParen,
    RightParen,
    Comma,
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Less,
    Greater,
    LessEqual,
    GreaterEqual,
    EqualEqual,
    Eof,
}
